package org.sk.jih.modal;

public class ImageUpload {
    String id, fileName, parkVehicleId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getParkVehicleId() {
        return parkVehicleId;
    }

    public void setParkVehicleId(String parkVehicleId) {
        this.parkVehicleId = parkVehicleId;
    }
}
